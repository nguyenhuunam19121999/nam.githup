// Map: PDF lesson # → {week, lesson} in our app
// PDF 26課 → week1 lesson1, 27課 → week1 lesson2, ... 50課 → week4 lesson24
function wl(pdfLesson) {
  const n = pdfLesson - 25; // 1..25
  const week = Math.ceil(n / 6);
  const lesson = n;
  return { week: Math.min(week, 4), lesson: Math.min(lesson, 24) };
}

// Helper: create vocab entry
function v(kanji, hira, han, nghia, pdfLesson) {
  const { week, lesson } = wl(pdfLesson);
  return { kanji, hira, han, nghia, lesson, week };
}

