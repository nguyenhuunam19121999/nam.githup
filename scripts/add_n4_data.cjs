const grammarPath = path.join(__dirname, '../artifacts/mirai-jp/assets/data_nn/n4.json');

const existingVocab = JSON.parse(fs.readFileSync(vocabPath, 'utf8'));
const existingGrammar = JSON.parse(fs.readFileSync(grammarPath, 'utf8'));

// Build a set of existing vocab kanji+hira keys to avoid exact duplicates
const vocabKeys = new Set(existingVocab.map(v => v.kanji + '|' + v.lesson));
let addedVocab = 0;
const toAdd = [];
for (const item of newVocab) {
  const key = item.kanji + '|' + item.lesson;
  if (!vocabKeys.has(key)) {
    toAdd.push(item);
    vocabKeys.add(key);
    addedVocab++;
  }
}
const mergedVocab = [...existingVocab, ...toAdd];
fs.writeFileSync(vocabPath, JSON.stringify(mergedVocab, null, 2), 'utf8');
console.log(`Vocab: was ${existingVocab.length}, added ${addedVocab}, now ${mergedVocab.length}`);

// Grammar: add by id
const grammarIds = new Set(existingGrammar.map(g => g.id));
let addedGrammar = 0;
const grammarToAdd = [];
for (const item of newGrammar) {
  if (!grammarIds.has(item.id)) {
    grammarToAdd.push(item);
    grammarIds.add(item.id);
    addedGrammar++;
  }
}
const mergedGrammar = [...existingGrammar, ...grammarToAdd];
fs.writeFileSync(grammarPath, JSON.stringify(mergedGrammar, null, 2), 'utf8');
console.log(`Grammar: was ${existingGrammar.length}, added ${addedGrammar}, now ${mergedGrammar.length}`);
